# read_pdx_file() geneates a standaridzed data table from the raw CVR data downloaded
# from Multooman County at https://multco.us/info/turnout-and-statistics-november-2024-general-election
# the function deletes records with a value of "undervote" for all six rankings

read_pdx_file <- function(pdx_csv_file) {
  
    read_csv(pdx_csv_file, show_col_types = FALSE) |> 
      select(recordID = "Record Id", precinct = Precinct, 
           rank_1 = "Rank 1",
           rank_2 = "Rank 2",
           rank_3 = "Rank 3",
           rank_4 = "Rank 4",
           rank_5 = "Rank 5",
           rank_6 = "Rank 6" ) |> 
          
           filter(!if_all(rank_1:rank_6, ~ .x == "undervote"))
           
}

