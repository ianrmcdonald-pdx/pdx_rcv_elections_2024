# generate an stv object from the preprocessed data tables
#.compute_stv() uses pivot functions to produce one column per candidate
# eliminates overvotes and undervotes (using NA instead)
# ensures that rankings do not have gaps
# and each candidate is ranked only once

# the tablulation of round by round results appears in a data table generated by the
# vote::summary() function

compute_stv <- function(office_inp, seats_num = 1, quota_const = TRUE) {
 
   office <- office_inp |> 
    pivot_longer(cols = !c(recordID, precinct), 
               names_to = "choice_num", values_to = "choice") |> 
    mutate(choice_num = as.numeric(str_sub(choice_num, -1, -1))) |> 
    filter(choice != "undervote") |> 
    filter(!str_detect(choice, "\n")) |> 
    
    group_by(recordID, precinct, choice) |> 
      slice_min(choice_num, with_ties = FALSE) |> 
    ungroup() |> 
    
    group_by(recordID, precinct) |> 
      mutate(choice_num = rank(choice_num)) |> 
    ungroup()


  office <- office |> 
    pivot_wider(id_cols = c("recordID", "precinct"),
              names_from = choice,
              values_from = choice_num) 

  office  |> 
    select(!c(recordID, precinct)) |> 
    vote::stv(nseats=seats_num, constant.quota = quota_const, quiet=TRUE)

}


